const cor = "Rubro-Negro";

// O switch é como um if, mas ele é mais utilizado quando temos muitas condições para verificar
switch (cor) {
    case "Vermelho":
        console.log("A cor é Vermelha");
        break;

    case "Azul":
        console.log("A cor é Azul");
        break;

    default:
        console.log("Cor não encontrada");
}